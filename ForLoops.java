public class ForLoops {

	public static void main(String[] args) {
		
		for(int i=0; i < 5; i = i + 1) {
			//Part 1- code executes before loop starts, only once; Part 2- Condition for true/false; Part 3- Executed once after every loop
			System.out.println("The value of i is: " + i );
			
		}

	}

}